#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst != (void *)0 && f != (void *)0)
	{
		if (lst->next != (void *)0)
			ft_lstiter(lst->next, f);
		f(lst->content);
	}
	return ;
}
