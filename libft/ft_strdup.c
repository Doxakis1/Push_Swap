#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	size;
	char	*final;

	size = ft_strlen(s);
	final = malloc(size + 1);
	if (final != (void *)0)
	{
		ft_strlcpy(final, s, (size + 1));
		return (final);
	}
	return ((void *)0);
}
