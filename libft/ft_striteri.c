#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	string_len;
	size_t	i;

	i = 0;
	if (s == (void *)0 || f == (void *)0)
		return ;
	string_len = ft_strlen(s);
	while (i < string_len)
	{
		f(i, &s[i]);
		i++;
	}
}
